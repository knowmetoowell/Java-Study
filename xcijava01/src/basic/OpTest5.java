package basic;

public class OpTest5 {
    public static void main(String[] args) {
        int i = 5;
        System.out.println(i);
        i = i + 1;
        System.out.println(i);
        i += 1;
        System.out.println(i);
        i++;
        System.out.println(i);
        ++i;
        System.out.println(i);

        int n = 10;
        n *= 2;
        System.out.println(n);

    }
}
